    <b>Cambodia</b>( Khmer: កម្ពុជា ), officially the Kingdom of Cambodia
    (Khmer: ព្រះរាជាណាចក្រកម្ពុជា), is a country located in the southern
    portion of the Indochina peninsula in Southeast Asia. It is 181,035
    square kilometres (69,898 square miles) in area, bordered by Thailand
    to the northwest, Laos to the northeast, Vietnam to the east and the
    Gulf of Thailand to the southwest. The sovereign state of Cambodia has
    a population of over 15 million. Buddhism is enshrined the
    constitution as the official state religion, and is practised by more
    than 97% of the population. Cambodia's minority groups include
    Vietnamese, Chinese, Chams and 30 hill tribes. The capital and largest
    city is Phnom Penh, the political, economic and cultural centre of
    Cambodia. The kingdom is an elective constitutional monarchy with a
    monarch, currently Norodom Sihamoni, chosen by the Royal Council of
    the Throne as head of state. The head of government is the Prime
    Minister, currently Hun Sen, the longest serving non-royal leader in
    Southeast Asia, ruling Cambodia since 1985. In 802 AD, Jayavarman II
    declared himself king, uniting the warring Khmer princes of Chenla
    under the name "Kambuja".[10] This marked the beginning of the Khmer
    Empire, which flourished for over 600 years, allowing successive kings
    to control and exert influence over much of Southeast Asia and
    accumulate immense power and wealth. The Indianised kingdom
    facilitated the spread of first Hinduism and then Buddhism to much of
    Southeast Asia and undertook many religious infrastructural projects
    throughout the region, including the construction of more than 1,000
    temples and monuments in Angkor alone. Angkor Wat is the most famous
    of these structures and is designated as a World Heritage Site. In the
    fifteenth century, after the rebellion of Ayutthaya, which was
    formerly ruled by the Khmer Empire, Cambodia experienced the decline
    of power. Cambodia faced two increasingly powerful neighbors,
    Ayutthaya of Thailand and Nguyen dynasty of Vietnam, and marking the
    downturn of Cambodia's fate. In 1863, Cambodia became a protectorate
    of France, and later was incorporated into French Indochina of
    Southeast Asia. Cambodia gained independence from France in 1953. The
    Vietnam War extended into the country in 1965 with the expansion of
    the Ho Chi Minh Trail and establishment of the Sihanouk Trail. This
    led to the US bombing of Cambodia from 1969 until 1973. Following the
    Cambodian coup of 1970 which installed the right-wing pro-US Khmer
    Republic, the deposed King Sihanouk gave his support to his former
    enemies, the Khmer Rouge. With the support of the monarchy and North
    Vietnam, the Khmer Rouge emerged as a major power, taking Phnom Penh
    in 1975. The Khmer Rouge then carried out the Cambodian genocide from
    1975 until 1979, when they were ousted by Vietnam and the
    Vietnamese-backed People's Republic of Kampuchea, supported by the
    Soviet Union, in the Cambodian–Vietnamese War. Following the 1991
    Paris Peace Accords, Cambodia was governed briefly by a United Nations
    mission (1992–93). The UN withdrew after holding elections in which
    around 90 percent of the registered voters cast ballots. The 1997
    factional fighting resulted in the ousting of the government by Prime
    Minister Hun Sen and the Cambodian People's Party, who remain in power
    as of April 2020. Cambodia is a member of the United Nations since
    1955, ASEAN, the East Asia Summit, the WTO, the Non-Aligned Movement
    and La Francophonie. According to several foreign organisations, the
    country has widespread poverty, pervasive corruption, lack of
    political freedoms, low human development and a high rate of hunger.
    Cambodia has been described by Human Rights Watch's Southeast Asian
    Director, David Roberts, as a "relatively authoritarian coalition via
    a superficial democracy". Constitutionally a multi-party liberal
    democracy, the country is de facto governed under one-party rule as of
    2018.
